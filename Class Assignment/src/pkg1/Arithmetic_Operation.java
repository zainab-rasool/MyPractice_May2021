package pkg1;

public class Arithmetic_Operation {
	public int add(int a, int b) {
		int c=a+b;
		return c;	
	}
	
	public int sub(int a, int b) {
		int c=a-b;
		return c;	
	}
	public int multiply(int a, int b) {
		int c=a*b;
		return c;	
	}
	public float divide(int a, int b) {
		float c=a/b;
		return c;	
	}

}
